public class java中的常量 {
    // 可以理解为是一种特殊的变量，它的值被设定后，在程序运行过程中不允许改变。
    // final String LOVE = "xiangyuan";
    // 常量一般为大写字符
    public static void main(String[] args){
        final String LOVE = "xiangyuan";
        final double PI = 3.14;
        System.out.println(LOVE);
        System.out.println(PI);
    }
}
