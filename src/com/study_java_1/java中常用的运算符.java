public class java中常用的运算符 {
    public static void main(String[] args){
        /*
        *  +、-、*、/、%、++、--
        * */
        int a = 6;
        int b = ++a;
        int c = ++a;
       // 自增和自减运算符只能用于操作变量
        System.out.println(b);
        System.out.println(c);
    }
}
