public class java中的数据类型 {
    /**
     * java中的数据类型
     *      基本数据类型（存的数据本身）
     *          数值型
     *              整数类型（byte、short、int、long）
     *              浮点类型（float 单精度如身高、double双精度，平均分、价格等）
     *          字符型（char）
     *          布尔型（boolean）
     *      引用数据类型（存的数据空间地址）
     *          类（class）
     *          接口（interface）
     *          数组
     */
    public static void main(String[] args){
        // 为float变量赋值的时候，后面加上f
        float height = 175.2f;
        // 为char变量赋值的时候用单引号，用于存储单个字符
        char level = 'A';
        System.out.println(level);
        System.out.println(height);
    }

}
